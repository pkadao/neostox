package pattern;

public class Eg2 {

	public static void main(String[] args)
	{
         //* * * * *
		 //* * * * *
		 //* * * * *
		 //* * * * *
		 //* * * * *
		
		//row=5 columns=5
		//outer for loop-->rows
		
		for(int row=1;row<=5;row++)
		{
			//iner for loop -->columns
			
			for(int column=1;column<=5;column++);
			{
				System.out.print("* ");
			}
			System.out.println();
		}
	}

}
